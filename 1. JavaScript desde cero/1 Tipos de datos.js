// Tipos de datos en JavaScript

//console.log es una funcion que se utiliza para imprimir en consola

// Ejemplo 1: número
console.log(typeof 42); // number

// Ejemplo 2: cadena de texto
console.log(typeof 'Veinticinco'); // string

// Ejemplo 3: número negativo
console.log(typeof -666); // number

// Ejemplo 4: valor booleano (true)
console.log(typeof true); // boolean

// Ejemplo 5: valor 0
console.log(typeof 0); // number

// Ejemplo 6: cadena vacía
console.log(typeof ''); // string

// Ejemplo 7: valor null
console.log(typeof null); // object

// Ejemplo 8: valor undefined
console.log(typeof undefined); // undefined

// Ejemplo 9: valor booleano FALSE 
console.log(typeof false); // boolean

// Ejemplo 10: un array 
console.log(typeof [1, 2, 3]); // object

// Ejemplo 11: función
console.log(typeof function() {}); // function

// Ejemplo 12: objeto literal
console.log(typeof {name: 'Juan', age: 25}); // object

// Ejemplo 13: símbolo
console.log(typeof Symbol('id')); // symbol

// Ejemplo 14: BigInt
console.log(typeof 1234567890123456789012345678901234567890n); // bigint




