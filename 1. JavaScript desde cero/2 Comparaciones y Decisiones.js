// Problema: Evaluador de Notas con Mensajes Personalizados

let nota = 85; 

if (nota >= 90) {
    console.log("Excelente"); // Si la nota es 90 o más
} else if (nota >= 75) {
    console.log("Bien"); // Si la nota está entre 75 y 89
} else if (nota >= 60) {
    console.log("Suficiente"); // Si la nota está entre 60 y 74
} else {
    console.log("No aprueba"); // Si la nota es menor de 60
}
